#include "fractol.h"

int	ft_calcul(float *z, float *c)
{
	float	temp;

	temp = (z[0] * z[0]) - (z[1] * z[1]) + c[0];
	z[1] = (2 * z[0] * z[1]) + c[1];
	z[0] = temp;
	if (((z[0] * z[0]) + (z[1] * z[1])) > 4)
		return (1);
	return (0);
}
